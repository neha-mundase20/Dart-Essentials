import 'dart:ffi';

void main() {
  int age = 30;
  print(age);

  double number = 1.24;
  print(number);

  String value = "Dart Essentials";
  print(value);

  bool isNight = false;
  print(isNight);

  /*'dynamic' is a variable where datatype of the variable can be changed*/
  dynamic source = "The Alchemist";
  print(source);
  source = 32;
  print(source);
}
